let newContent = fileContent;
for (const lang of Object.keys(additions)) {
  const langKey = `${lang}: {`;
  const insertionPoint = newContent.indexOf(langKey) + langKey.length;
  
  let addedStrings = Object.entries(additions[lang])
    .map(([k, v]) => `\n    ${k}: "${v}",`)
    .join('');
    
  newContent = newContent.slice(0, insertionPoint) + addedStrings + newContent.slice(insertionPoint);
}

